package inheritance.sample;

public class Super {
    private  int x,y;

    public void setData(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Super() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
